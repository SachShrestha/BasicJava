package CoreJava;

public class Weather {
	
	public static void main (String[] args)
	{
		
	int weather1=25;
	switch (weather1) {
	
	case 125:
		System.out.println("Cloudy");
		break;
	case -5:
		System.out.println("Freezing");
		break;
		
	case 60:
		System.out.println("hot");
		break;
		
		default:
			System.out.println("I dont know the temperature");
	}
	
	
		
		
		
		
		
		
		
		
	}

}
